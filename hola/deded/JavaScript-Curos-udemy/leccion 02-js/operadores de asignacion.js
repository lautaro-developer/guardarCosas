// los operadores de asignacion son ' = ' y dan el valor a la variables
let a = 10;

// operador de asignacion compuesto


// esto es igual a => a = ( a+2 )
a += 2; 
console.log(a)

// esto es igual a => a = ( a-2 )
a -= 2;
console.log(a)

// esto es igual a => a = ( a*2 )
a *=2;
console.log(a)

// esto es igual a => a = ( a**2 )
a **= 2;
console.log(a)

// esto es igual a => a = ( a/2 )
a /= 2;
console.log(a)

// esto es igual a => a = ( a%2 )
a %= 2;
console.log(a)